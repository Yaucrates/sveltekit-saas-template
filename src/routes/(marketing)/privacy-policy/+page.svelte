	<h2 id="students">10. Students and FERPA</h2>

	<p>
		In the United States, certain student information may be governed by the Family Educational
		Rights and Privacy Act (FERPA). FERPA protects the privacy of student education records and
		gives parents and eligible students certain rights with respect to their education records.
	</p>

	<p>
		When Impart provides services to educational institutions, we may act as a "school official"
		with a legitimate educational interest in student records. In such cases, the educational
		institution remains responsible for FERPA compliance, and Impart processes student information
		under the institution's direction and in accordance with FERPA requirements.
	</p>

	<p>
		For more information about FERPA, please visit the U.S. Department of Education's FERPA website
		at: <a href="https://studentprivacy.ed.gov/ferpa" target="_blank"
			>https://studentprivacy.ed.gov/ferpa</a
		>
	</p>

