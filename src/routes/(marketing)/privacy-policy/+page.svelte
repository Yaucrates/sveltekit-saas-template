		technologies, or from other sources. This section explains more about our sources of personal
		information.
	</p>

	<h3>Information Provided Directly</h3>

	<p>
		We collect personal information directly from people who use our platform or services, visit our
		offices, or communicate with us. All information we ask for is mandatory unless marked as
		optional. If mandatory information is not provided, we may be unable to provide the requested
		service.
