		through our third-party analytics providers. This includes IP address, browser type and
		settings, device details, internet service provider, referring and exit pages, platform content
		interactions, location data, and other usage information. We use tracking technologies such as
		cookies, web beacons, embedded scripts, and device recognition technologies. We use this
		information to enable platform functionality, understand usage trends, personalize user
		experiences, improve our services, and ensure platform security.
	</p>

	<h3>Information From Other Sources</h3>

	<p>
